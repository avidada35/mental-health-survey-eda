def normalize_gender(g):
    g = str(g).lower().strip()
    if any(x in g for x in [
        'female', 'woman', 'f', 'girl', 'femal',
        'cis f', 'cis female', 'femake', 'female (cis)', 'woman'
    ]):
        return 'Female'
    elif any(x in g for x in [
        'male', 'man', 'm', 'boy', 'cis m',
        'cis male', 'maile', 'mal', 'male (cis)', 'make'
    ]):
        return 'Male'
    else:
        return 'Other'

df['Gender'] = df['Gender'].apply(normalize_gender)

print("\n=== Gender Value Counts After Normalization ===")
print(df['Gender'].value_counts())


def grouped_pct_bar(ax, df, group_col, target_col, title, colors):
    ct = df.groupby([group_col, target_col]).size().unstack(fill_value=0)
    ct_pct = ct.div(ct.sum(axis=1), axis=0) * 100

    ct_pct.plot(kind='bar', ax=ax, color=colors, edgecolor='white',
                width=0.6, rot=0)

    for container in ax.containers:
        for bar in container:
            h = bar.get_height()
            if h > 1:
                ax.text(bar.get_x() + bar.get_width() / 2,
                        bar.get_height() + 0.8,
                        f'{h:.1f}%', ha='center', fontsize=9, fontweight='bold')

    ax.set_title(title, fontsize=13, fontweight='bold')
    ax.set_xlabel(group_col.replace('_', ' ').title())
    ax.set_ylabel('Percentage (%)')
    ax.set_ylim(0, 110)
    ax.legend(title=target_col.title(), loc='upper right')

